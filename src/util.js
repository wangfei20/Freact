export function isEvent(attr){
    return attr == "onclick"
        || attr == "ondblclick"
        || attr == "onmousedown"
        || attr == "onmouseup"
        || attr == "onmouseover"
        || attr == "onmousemove"
        || attr == "onmouseout"
        || attr == "onkeypress"
        || attr == "onkeydown"
        || attr == "onkeyup"
        || attr == "onfocus"
        || attr == "onblur"
        || attr == "onchange"
        || attr == "onselect"
        || attr == "onsubmit"
        || attr == "onreset"
        || attr == "onerror"
        || attr == "onload"
        || attr == "onunload"
        || attr == "onscroll"
        || attr == "oncontextmenu"
        || attr == "ondragstart"
        || attr == "ondragend"
        || attr == "ondragover"
        || attr == "ondragenter"
        || attr == "ondragleave"
        || attr == "ondrop"
}